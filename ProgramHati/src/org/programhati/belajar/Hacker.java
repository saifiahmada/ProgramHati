package org.programhati.belajar;

/** @author Saifi Ahmada Mar 28, 2013 10:50:08 AM  **/

public class Hacker {
	
	private String idHacker;
	
	private String nama;
	
	private boolean happy;
	
	private boolean sad;

	public String getIdHacker() {
		return idHacker;
	}

	public void setIdHacker(String idHacker) {
		this.idHacker = idHacker;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public boolean isHappy() {
		return happy;
	}

	public void setHappy(boolean happy) {
		this.happy = happy;
	}

	public boolean isSad() {
		return sad;
	}

	public void setSad(boolean sad) {
		this.sad = sad;
	} 
	
}

